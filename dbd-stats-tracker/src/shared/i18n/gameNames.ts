/**
 * One flat dictionary per language. Killer names today; perk/addon/item entries
 * can be merged in here later (names are globally unique across categories).
 */
const DICTIONARIES: Record<Exclude<GameNameLanguage, "en">, Record<string, string>> = {
  fr: { ...KILLERS_FR, ...PERKS_FR, ...ITEMS_FR, ...ADDONS_FR },
  de: { ...KILLERS_DE, ...PERKS_DE, ...ITEMS_DE, ...ADDONS_DE },
  it: { ...KILLERS_IT, ...PERKS_IT, ...ITEMS_IT, ...ADDONS_IT },
  es: { ...KILLERS_ES, ...PERKS_ES, ...ITEMS_ES, ...ADDONS_ES },
};

/** Display name for a game entity; falls back to the canonical English name when no entry exists. */
export function translateGameName(name: string, language: GameNameLanguage): string {
  if (language === "en") return name;
  return DICTIONARIES[language][name] ?? name;
}

/**
 * Equivalence table for typed input: maps any known display name (from ANY
 * language, case-insensitive) back to its canonical English name. Canonical
 * names map to themselves, so mixed-language typing always resolves.
 */
const REVERSE_LOOKUP: Record<string, string> = (() => {
  const map: Record<string, string> = {};
  for (const dictionary of Object.values(DICTIONARIES)) {
    for (const [canonical, display] of Object.entries(dictionary)) {
      map[display.toLowerCase()] = canonical;
      map[canonical.toLowerCase()] = canonical;
    }
  }
  return map;
})();

/**
 * Canonical English name for a typed value: resolves translated names (any
 * language) and case variants of known names; unknown text passes through
 * unchanged so free typing is never blocked.
 */
export function canonicalGameName(input: string): string {
  return REVERSE_LOOKUP[input.toLowerCase()] ?? input;
}
